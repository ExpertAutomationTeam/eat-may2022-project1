package datatype.nonprimtive.wrapperclasses;

public class WrapperClasses {

    public static void main(String[] args) {

        //wrapper class is a class that wraps a primitive variable in order to make it non-primitive
        //numbers
        //wholes numbers
        Byte b = 127;//1 byte
        Short s = 1234;//2 bytes
        Integer x = 123440;//4 bytes
        Long l = 23416624352342L;//8 bytes

        //fractions
        Float f = 1.62547284F;//4 bytes
        Double y = 10.5;//8 bytes

        //characters
        Character c = '1';//2 bytes

        //boolean
        Boolean boo = false;//1 bit (1/8 byte)

    }
}
